JUNÇÃO = 

EXEMPLOS:
SELECT NOME, SEXO, BAIRRO, CIDADE # PROJEÇÃO
FROM CLIENTE # ORIGEM
INNER JOIN ENDERECO ON ID_CLIENTE = FK_CLIENTE #JUNÇÃO
