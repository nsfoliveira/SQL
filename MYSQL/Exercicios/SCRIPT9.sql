USE COMERCIO;

SELECT NOME, SEXO, BAIRRO, CIDADE # PROJEÇÃO
FROM CLIENTE # ORIGEM
INNER JOIN ENDERECO ON ID_CLIENTE = FK_CLIENTE #JUNÇÃO
WHERE SEXO = 'F'; # SELEÇÃO


SELECT NOME, SEXO, EMAIL, TIPO, NUMERO
FROM CLIENTE
INNER JOIN TELEFONE ON ID_CLIENTE = FK_CLIENTE;


SELECT CLIENTE.NOME, CLIENTE.SEXO, ENDERECO.BAIRRO, ENDERECO.CIDADE, TELEFONE.TIPO, TELEFONE.NUMERO
FROM CLIENTE
INNER JOIN ENDERECO 
ON  CLIENTE.ID_CLIENTE = ENDERECO.FK_CLIENTE 
INNER JOIN TELEFONE 
ON CLIENTE.ID_CLIENTE = TELEFONE.FK_CLIENTE;



SELECT C.NOME, C.SEXO, E.BAIRRO, E.CIDADE, T.TIPO, T.NUMERO
FROM CLIENTE C
INNER JOIN ENDERECO E
ON  C.ID_CLIENTE = E.FK_CLIENTE 
INNER JOIN TELEFONE T
ON C.ID_CLIENTE = T.FK_CLIENTE;


